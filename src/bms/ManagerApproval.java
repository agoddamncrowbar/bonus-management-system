    private void Add_Emp_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_Emp_btnActionPerformed
        try {
            DBconn();
            ps = con.prepareStatement("insert into bonus_table (emp,e_code,dept,period,b_sal,sales,approved,bonus,net_sal)values(?,?,?,?,?,?,?,?,?)");
            ps.setString(1,addname_txt.getText());
            ps.setString(2,adde_code_txt.getText());
            ps.setString(3,adddept_txt.getText());
            ps.setString(4,"0 months");
            ps.setString(5,addsalary_txt.getText());
            ps.setString(6,"0");
            ps.setString(7,"no");
            ps.setString(8,addbonus_txt.getText());
            ps.setString(9,addsalary_txt.getText());
            ps.executeUpdate();
            tableData();

        } catch (SQLException ex) {
            Logger.getLogger(ManagerApproval.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Add_Emp_btnActionPerformed

    private void Approve_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Approve_btnActionPerformed
        int bonus = Integer.parseInt(bonus_txt.getText());
        int sal = Integer.parseInt(salary_txt.getText());
        int sales = Integer.parseInt(sales_txt.getText());
        int net_sal= (bonus*sales)+sal;
        String netsal = Integer.toString(net_sal);
        String approve = "yes";
            
        try {        
            
            DBconn();
            
            ps = con.prepareStatement("update bonus_table set approved=?, net_sal=? where e_code =? and bcode=?");
            ps.setString(1, approve);
            ps.setString(2, netsal);
            ps.setString(3, ecode_txt.getText());
            ps.setString(4, bcode); // Assuming bcode is a variable containing a string value
            ps.executeUpdate();
            
        } catch (SQLException ex) {
            Logger.getLogger(ManagerApproval.class.getName()).log(Level.SEVERE, null, ex);
        }
        tableData();        
    }//GEN-LAST:event_Approve_btnActionPerformed

