    public void tableData()
    {
        DBconn();
        try {
            DefaultTableModel tbModel = (DefaultTableModel)bms_table.getModel();
            tbModel.setRowCount(0);
            Statement st = con.createStatement();
            String ss ="Select * from bonus_table";
            rs=ps.executeQuery(ss);
            while (rs.next()){
                int id = rs.getInt("ID");
                String Employee = String.valueOf(rs.getString("emp"));
                String E_code = String.valueOf(rs.getString("e_code"));
                String Department = String.valueOf(rs.getString("dept"));
                String Period = String.valueOf(rs.getString("period"));
                String Base_Sal = String.valueOf(rs.getString("b_sal"));
                String Sales = String.valueOf(rs.getString("sales"));
                String Approved = String.valueOf(rs.getString("approved"));
                String Bonus = String.valueOf(rs.getString("bonus"));
                String Net_Sal = String.valueOf(rs.getString("net_sal"));
                bcode = String.valueOf(rs.getString("bcode"));
                String ID = String.valueOf(id);
                String tbData[] = {ID,Employee,E_code,Department,Period,Base_Sal,Sales,Approved,Bonus,Net_Sal,bcode};
                tbModel.addRow(tbData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManagerApproval.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
